package org.be.kuleuven.hci.openbadges.persistanlayer;

import java.util.Hashtable;

import org.be.kuleuven.hci.openbadges.badges.Badge;

import com.googlecode.objectify.Objectify;
import com.googlecode.objectify.ObjectifyFactory;
import com.googlecode.objectify.ObjectifyService;

public class OfyService {
	
	private static OfyService _ofyService;
    
	OfyService (){

        factory().register(Badge.class);

    }
	
	public static synchronized OfyService getOfyService() {
		if (_ofyService == null) {
			_ofyService = new OfyService();
			
		}
		return _ofyService;
	}

    public static Objectify ofy() {
        return ObjectifyService.ofy();
    }

    public static ObjectifyFactory factory() {
        return ObjectifyService.factory();
    }
    
}